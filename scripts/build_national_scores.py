def batch_for(mode: str) -> str:
    return "本科批" if mode == "新高考" else "普通类一批"


def build() -> dict:
    records = []
    for year, rows in [(2024, Y2024), (2023, Y2023), (2022, Y2022)]:
        for province, mode, category, control, minimum, note in rows:
            records.append(
                {
                    "year": year,
                    "province": province,
                    "exam_mode": mode,
                    "category": category,
                    "batch": batch_for(mode),
                    "control_line": control,
                    "min_score": minimum,
                    "min_rank": None,
                    "note": note,
                }
            )
    provinces = sorted({r["province"] for r in records})
    return {
        "meta": {
            "source": "https://gaokao.chsi.com.cn/zsgs/mdgs.jsp",
            "updated": date.today().isoformat(),
            "provinces_covered": provinces,
            "years": [2024, 2023, 2022],
            "note": "普通本科批/一批最低录取分，来源于川大招生网公布统计，仅供参考",
        },
        "records": records,
    }


def main() -> None:
    payload = build()
    OUT.write_text(json.dumps(payload, ensure_ascii=False, indent=2), encoding="utf-8")
    print(f"Wrote {len(payload['records'])} records, {len(payload['meta']['provinces_covered'])} provinces -> {OUT}")


if __name__ == "__main__":
    main()
